    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        // TODO add your handling code here:

        if (txt_id.getText().isEmpty() || txt_nombre.getText().isEmpty() || txt_instrumento.getText().isEmpty() || txt_lugarNacimiento.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null,
                     "No hay datos en el id, nombre o instrumento",
                    "Faltan datos",
                    JOptionPane.ERROR_MESSAGE);

        } else {

            //comprobar si el checkbox está activo
            if (!ckbx_muerto.isSelected()) {
                Date fechainicial = (Date) spn_fechaNacimiento.getValue();
                Date fechafin = (Date) spn_fechaMuerte.getValue();
                //comprobar si la fecha final es mayor que la de nacimiento o creacion
                if (fechainicial.before(fechafin)) {
                    //si es correcto hacer la sql
                    Musico m = new Musico();
                    m.setId(Integer.valueOf(txt_id.getText()));
                    m.setNombre(txt_nombre.getText());
                    m.setInstrumento(txt_instrumento.getText());
                    m.setLugarnacimiento(txt_lugarNacimiento.getText());
                    m.setFecha_nacimiento(new SimpleDateFormat("yyyy-MM-dd").format((Date) spn_fechaNacimiento.getValue()));
                    m.setFecha_muerte(new SimpleDateFormat("yyyy-MM-dd").format((Date) spn_fechaMuerte.getValue()));
                    controlador.introducirMusico(m);
                    new Thread(() -> {
                        new VentanaEmergente(new JFrame(), false, "Creado correctamente").setVisible(true);
                    }).start();
                    //si no, no hacer la sql
                } else {

                    JOptionPane.showMessageDialog(null,
                             "La fecha de nacimiento es mayor que la de muerte",
                            "Error en las fechas",
                            JOptionPane.ERROR_MESSAGE);
                }
                //si no está seleccionado hacer la sql
            } else {
                new Thread(() -> {
                    new VentanaEmergente(new JFrame(), false, "Creado correctamente").setVisible(true);
                }).start();
                Musico m = new Musico();
                m.setId(Integer.valueOf(txt_id.getText()));
                m.setNombre(txt_nombre.getText());
                m.setInstrumento(txt_instrumento.getText());
                m.setLugarnacimiento(txt_lugarNacimiento.getText());
                m.setFecha_nacimiento(new SimpleDateFormat("yyyy-MM-dd").format((Date) spn_fechaNacimiento.getValue()));
                controlador.introducirMusico(m);
            }

        }
        actualizarTabla();

    }//GEN-LAST:event_btn_insertActionPerformed

