    public void actualizarTabla() {
        try {
            ArrayList<Musico> musicos = controlador.sacarMusicos();

            DefaultTableModel modelo = (DefaultTableModel) list_musicos.getModel();
            for (int i = 0; i < list_musicos.getRowCount(); i++) {
                modelo.removeRow(i);
                i -= 1;
            }
            for (int i = 0; i < musicos.size(); i++) {
                String[] g1 = {String.valueOf(musicos.get(i).getId()), musicos.get(i).getNombre()};
                modelo.addRow(g1);
            }
        } catch (Exception e) {
        }

    }

