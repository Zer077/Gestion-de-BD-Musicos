    private void listaRelacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaRelacionMouseClicked
        // TODO add your handling code here:
        //Selecciona de la tabla de las relaciones
        seleccionado2 = listaRelacion.getSelectedRow();
        ArrayList<String> respuesta = null;
        //Si he insertado musicos en la tabla...

        if (insertaMusicos) {
            System.out.println(Integer.valueOf((String) listaRelacion.getModel().getValueAt(seleccionado2, 0)));
            respuesta = controlador.sacarMusicoGrupo(idgrupo, Integer.valueOf((String) listaRelacion.getModel().getValueAt(seleccionado2, 0)));

        } else {
            respuesta = controlador.sacarMusicoGrupo(Integer.valueOf((String) listaRelacion.getModel().getValueAt(seleccionado2, 0)), idmusico);

        }
        //Inserta los datos que ha recogido al clicar en una fila de la tabla de relaciones
        //Respuesta tiene 3 campos, instrumento, fecha inicio, fecha final
        txt_intrumento.setText(respuesta.get(0));
        String fecha = respuesta.get(1);
        //Spliteo la fecha para poder interoducirla al Gregoria Calendar
        String[] fechaseparada = fecha.split("-");
        Calendar calendar = new GregorianCalendar(Integer.valueOf(fechaseparada[0]), Integer.valueOf(fechaseparada[1]), Integer.valueOf(fechaseparada[2]));
        spn_inicio.setValue(calendar.getTime());
        System.out.println(respuesta.get(2));
        //Si no me devuelve ninguna fecha
        if (respuesta.get(2) != null) {
            fecha = respuesta.get(2);
            ckbx_grupo.setSelected(false);

            fechaseparada = fecha.split("-");
            calendar = new GregorianCalendar(Integer.valueOf(fechaseparada[0]), Integer.valueOf(fechaseparada[1]), Integer.valueOf(fechaseparada[2]));
            spn_fin.setValue(calendar.getTime());

        } else {
            ckbx_grupo.setSelected(true);

        }

    }//GEN-LAST:event_listaRelacionMouseClicked

    //Este metodo será usado cuando lo llame un Grupo
    public void insentarMusicos(int idgrupo) {

        //Inserto Musicos a la tabla
        listado.setText("Listado de Músicos");
        setTitle("Gestión con Músicos");
        relacion.setText("Músicos en el grupo: " + controlador.sacarGrupo(idgrupo).getNombre());
        this.idgrupo = idgrupo;
        insertaMusicos = true;
        ArrayList<Musico> musicos = controlador.sacarMusicos();

        DefaultTableModel modelo = (DefaultTableModel) listaDatos.getModel();
        for (int i = 0; i < listaDatos.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
        for (int i = 0; i < musicos.size(); i++) {
            String[] g1 = {String.valueOf(musicos.get(i).getId()), musicos.get(i).getNombre()};
            modelo.addRow(g1);

        }
        insertarRelacionMusicoGrupo();
    }

    //Este metodo será usado cuando lo llame un Musico
    public void insertarGrupos(int idmusico) {
        //Inserto grupos a la tabla
        listado.setText("Listado de Grupos");
        setTitle("Gestión con Grupos");

        relacion.setText("Grupos del músico: " + controlador.sacarMusico(idmusico).getNombre());
        txt_intrumento.setText(controlador.sacarMusico(idmusico).getInstrumento());
        this.idmusico = idmusico;
        insertaMusicos = false;
        ArrayList<Grupo> grupos = controlador.sacarGrupos();

        DefaultTableModel modelo = (DefaultTableModel) listaDatos.getModel();
        for (int i = 0; i < grupos.size(); i++) {
            String[] g1 = {String.valueOf(grupos.get(i).getId()), grupos.get(i).getNombre()};
            modelo.addRow(g1);
            listaDatos.setModel(modelo);

        }
        insertarRelacionMusicoGrupo();

    }

    //Insertar la relacion entre el Musico y el Grupo
    public void insertarRelacionMusicoGrupo() {
//Si lo ha llamado un inserta musicos

        if (!insertaMusicos) {

            ArrayList<Grupo> grupos = controlador.sacarGrupoporMusico(idmusico);
            DefaultTableModel modelo = (DefaultTableModel) listaRelacion.getModel();
            for (int i = 0; i < listaRelacion.getRowCount(); i++) {
                modelo.removeRow(i);
                i -= 1;
            }
            for (int i = 0; i < grupos.size(); i++) {
                String[] g1 = {String.valueOf(grupos.get(i).getId()), grupos.get(i).getNombre()};
                modelo.addRow(g1);
                listaRelacion.setModel(modelo);

            }
//Si lo ha llamado un inserta grupos 
        } else {
            ArrayList<Musico> musicos = controlador.sacarMusicoporGrupo(idgrupo);
            DefaultTableModel modelo = (DefaultTableModel) listaRelacion.getModel();
            for (int i = 0; i < listaRelacion.getRowCount(); i++) {
                modelo.removeRow(i);
                i -= 1;
            }
            for (int i = 0; i < musicos.size(); i++) {
                String[] g1 = {String.valueOf(musicos.get(i).getId()), musicos.get(i).getNombre()};
                modelo.addRow(g1);
                listaRelacion.setModel(modelo);
            }

        }
    }
