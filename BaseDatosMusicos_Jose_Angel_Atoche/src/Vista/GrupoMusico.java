    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed

        if (!txt_intrumento.getText().isEmpty()) {

            String fecha_final = null;
            String fecha_inicio = new SimpleDateFormat("yyyy-MM-dd").format((Date) spn_inicio.getValue());
            if (ckbx_grupo.isSelected()) {
            } else {
                fecha_final = new SimpleDateFormat("yyyy-MM-dd").format((Date) spn_fin.getValue());
            }
            //Si he insertado musicos en la tabla...
            if (insertaMusicos) {

                controlador.introducirGrupoMusico(idgrupo, Integer.valueOf(String.valueOf(listaDatos.getModel().getValueAt(seleccionado, 0))), txt_intrumento.getText(), fecha_inicio, fecha_final);

            } else {
                controlador.introducirGrupoMusico(Integer.valueOf((String) listaDatos.getModel().getValueAt(seleccionado, 0)), idmusico, txt_intrumento.getText(), fecha_inicio, fecha_final);

            }
            insertarRelacionMusicoGrupo();
            new Thread(() -> {
                new VentanaEmergente(new JFrame(), false, "Creado correctamente").setVisible(true);
            }).start();
        } else {
            JOptionPane.showMessageDialog(null,
                    "NO HAY DATOS EN EL INSTRUMENTO",
                    "FALTAN DATOS",
                    JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btn_insertActionPerformed

