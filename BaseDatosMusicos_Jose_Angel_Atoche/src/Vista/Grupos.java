    private void list_grupoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_grupoMouseClicked
        //Cuando clicas un grupo pone los datos en la parte izquierda
        Grupo g = controlador.sacarGrupo(Integer.valueOf(String.valueOf(list_grupo.getModel().getValueAt(list_grupo.getSelectedRow(), 0))));
        //Se establecen los datos en los campos concretos
        txt_id.setText(String.valueOf(g.getId()));
        txt_nombre.setText(g.getNombre());
        //Se le da formato a la fecha para ponerla en el spinner
        String fecha = g.getFormacion();
        String[] fechaseparada = fecha.split("-");
        //Se mete en un calendario para poder introducirla en el spinner
        Calendar calendar = new GregorianCalendar(Integer.valueOf(fechaseparada[0]), Integer.valueOf(fechaseparada[1]), Integer.valueOf(fechaseparada[2]));
        spn_inicio.setValue(calendar.getTime());
        //Si el campo desintagracion no es nulo introduce los datos en el spinner
        if (g.getDesintegracion() != null) {
            ckbx_vigente.setSelected(true);
            fecha = g.getDesintegracion();
            fechaseparada = fecha.split("-");
            Calendar calendar1 = new GregorianCalendar(Integer.valueOf(fechaseparada[0]), Integer.valueOf(fechaseparada[1]), Integer.valueOf(fechaseparada[2]));
            spn_fin.setValue(calendar1.getTime());
        } else {
            ckbx_vigente.setSelected(false);
        }

    }//GEN-LAST:event_list_grupoMouseClicked

