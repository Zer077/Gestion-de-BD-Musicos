    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        // TODO add your handling code here:
        if (txt_id.getText().isEmpty() || txt_nombre.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this,
                    "No hay datos en el ID o en el nombre",
                    "Faltan datos",
                    JOptionPane.ERROR_MESSAGE);

        } else {

            //comprobar si el checkbox está activo
            if (!ckbx_vigente.isSelected()) {
                Date fechainicial = (Date) spn_inicio.getValue();
                Date fechafin = (Date) spn_fin.getValue();
                //comprobar si la fecha final es mayor que la de nacimiento o creacion
                if (fechainicial.before(fechafin)) {
                    //si es correcto hacer la sql
                    Grupo g = new Grupo();
                    g.setId(Integer.valueOf(txt_id.getText()));
                    g.setNombre(txt_nombre.getText());

                    g.setFormacion(new SimpleDateFormat("yyyy-MM-dd").format((Date) spn_inicio.getValue()));

                    g.setDesintegracion(new SimpleDateFormat("yyyy-MM-dd").format((Date) spn_fin.getValue()));

                    controlador.introducirGrupo(g);
                    new Thread(() -> {
                        new VentanaEmergente(new JFrame(), false, "Creado correctamente").setVisible(true);
                    }).start();
                    //si no, no hacer la sql

                } else {

                    JOptionPane.showMessageDialog(null,
                            "La fecha de inicio es posterior a la del final",
                            "Error en Datos",
                            JOptionPane.ERROR_MESSAGE);
                }
                //si no está seleccionado hacer la sql
            } else {

                Grupo g = new Grupo();
                g.setId(Integer.valueOf(txt_id.getText()));
                g.setNombre(txt_nombre.getText());
                g.setFormacion(new SimpleDateFormat("yyyy-MM-dd").format((Date) spn_inicio.getValue()));
                controlador.introducirGrupo(g);
                new Thread(() -> {
                    new VentanaEmergente(new JFrame(), false, "Creado correctamente").setVisible(true);
                }).start();
            }

        }
        actualizarTabla();

    }//GEN-LAST:event_btn_insertActionPerformed

    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
        //Si el id no está vacío
        if (!txt_id.getText().isEmpty()) {
            int dialogResult = JOptionPane.showConfirmDialog(this,
                    "Va a eliminar este  ¿Está seguro?",
                    "ATENCIÓN",
                    JOptionPane.YES_NO_OPTION);
            //Si selecciono que si en el JOption panel 
            if (dialogResult == JOptionPane.YES_OPTION) {
                controlador.eliminarGrupo(Integer.valueOf(txt_id.getText()));
                actualizarTabla();
                //Vacia los campos
                txt_id.setText("");
                txt_nombre.setText("");

                new Thread(() -> {
                    new VentanaEmergente(new JFrame(), false, "Eliminado correctamente").setVisible(true);
                }).start();
            } else {
                JOptionPane.showMessageDialog(this,
                        "No ha seleccionado nada",
                        "Faltan datos",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
        actualizarTabla();

    }//GEN-LAST:event_btn_delActionPerformed

