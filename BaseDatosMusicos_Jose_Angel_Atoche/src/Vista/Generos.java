    public void actualizarTabla() {

        ArrayList<Genero> generos = controlador.sacarGeneros();

        DefaultTableModel modelo = (DefaultTableModel) list_generos.getModel();
        for (int i = 0; i < list_generos.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }

        for (int i = 0; i < generos.size(); i++) {
            String[] g1 = {String.valueOf(generos.get(i).getId()), generos.get(i).getDescripcion()};

            modelo.addRow(g1);
        }

    }

