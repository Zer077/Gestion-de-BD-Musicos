    private void btn_modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modActionPerformed
        if (txt_id.getText().isEmpty() || txt_descrip.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null,
                    "NO HAY DATOS EN EL ID O EN LA DESCRIPCION",
                    "FALTAN DATOS",
                    JOptionPane.ERROR_MESSAGE);

        } else {
            Genero g = new Genero();
            g.setId(Integer.valueOf(txt_id.getText()));
            g.setDescripcion(txt_descrip.getText());
            controlador.modificarGenero(g);
            new Thread(() -> {
                new VentanaEmergente(new JFrame(), false, "Modificado correctamente").setVisible(true);
            }).start();
            actualizarTabla();
        }
    }//GEN-LAST:event_btn_modActionPerformed

    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
        if (!txt_id.getText().isEmpty()) {
            int dialogResult = JOptionPane.showConfirmDialog(null,
                    "Va a eliminar este  ¿Está seguro?",
                    "ATENCIÓN",
                    JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {

                controlador.eliminarGenero(Integer.valueOf(txt_id.getText()));
                actualizarTabla();
                txt_id.setText("");
                txt_descrip.setText("");
                new Thread(() -> {
                    new VentanaEmergente(new JFrame(), false, "Eliminado correctamente").setVisible(true);
                }).start();
            }
        } else {
            JOptionPane.showMessageDialog(null,
                    "NO HA SELECCIONADO NADA",
                    "FALTAN DATOS",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_delActionPerformed

    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        if (txt_id.getText().isEmpty() || txt_descrip.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null,
                    "NO HAY DATOS EN EL ID O EN LA DESCRIPCION",
                    "FALTAN DATOS",
                    JOptionPane.ERROR_MESSAGE);

        } else {

            Genero g = new Genero();
            g.setId(Integer.valueOf(txt_id.getText()));
            g.setDescripcion(txt_descrip.getText());
            controlador.introducirGenero(g);
            if (controlador.sacarGenero(g.getId()) != null) {

                new Thread(() -> {
                    new VentanaEmergente(new JFrame(), false, "Ese ID ya está usado").setVisible(true);
                }).start();

            } else {
                new Thread(() -> {
                    new VentanaEmergente(new JFrame(), false, "Creado correctamente").setVisible(true);
                }).start();
            }

            actualizarTabla();
        }
    }//GEN-LAST:event_btn_insertActionPerformed

